package Day12;

public class PrimeNumber {
 public static void main(String[] args) {
      int no  = 2;
      int tem = 0;
      for(int i = 0; i<= no; i++) {
    	  if(no%2==0) {
    		  tem=tem+i;
    		  
    	  }}if(tem==0) {
    		  System.out.println("this is a prime number");
    	  }else {
    		  System.out.println("this is a prime not number");
    	  }
      
}
}
